  $("#Resultado5").show();

  var d100 = document.getElementById("accp").value;
  var rd100 = (50/100 * d100)  ;
  var rfd100 = (50 - rd100 );

 $("#dd").show();
 $("#dd").css("color", "rgb(207, 3, 3)");
 $("#dadon").html(' 100');
